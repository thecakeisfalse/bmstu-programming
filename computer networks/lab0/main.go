package main

import (
	"fmt"
	"log"
	"net/http"
	"strings"

	"github.com/SlyMarbo/rss"
)

func printRSS(w http.ResponseWriter) {
	rssObject, err := rss.Fetch("https://news.rambler.ru/rss/technology/")

	if err == nil {
		fmt.Fprintf(w, "<h1>%s</h1>", rssObject.Title)
		fmt.Fprintf(w, "<p>Количество новостей : %d</p>", len(rssObject.Items))
		for v := range rssObject.Items {
			item := rssObject.Items[v]

			fmt.Fprintf(w, "<h3>#%d: %s (%s)</h3>", v+1, item.Title, item.Date)
			fmt.Fprintf(w, "<p>%s</p>", item.Summary)
			fmt.Fprintf(w, "<a href=\"%s\">Читать далее</a>", item.Link)
		}
	}
}

func HomeRouterHandler(w http.ResponseWriter, r *http.Request) {
	r.ParseForm()
	fmt.Println(r.Form)
	fmt.Println("path", r.URL.Path)
	fmt.Println("scheme", r.URL.Scheme)
	fmt.Println(r.Form["url_long"])
	for k, v := range r.Form {
		fmt.Println("key:", k)
		fmt.Println("val:", strings.Join(v, ""))
	}
	printRSS(w)
}

func main() {
	http.HandleFunc("/", HomeRouterHandler)
	err := http.ListenAndServe(":9000", nil)
	if err != nil {
		log.Fatal("ListenAndServe: ", err)
	}
}
