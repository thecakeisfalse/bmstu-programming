package main

import "fmt"

func sift_down(n, v int, less func(i, j int) bool, swap func(i, j int)) {
	var l, r, u int
	for ; 2*v <= n; v = u {
		l, r = 2*v+1, 2*v+2
		if r < n && !less(r, l) {
			u = r
		} else {
			u = l
		}
		if !(u < n) || less(u, v) {
			break
		}
		swap(v, u)
	}
}

func hsort(n int, less func(i, j int) bool, swap func(i, j int)) {
	for i := n/2 - 1; i >= 0; i-- {
		sift_down(n, i, less, swap)
	}

	for i := n - 1; i > 0; i-- {
		swap(0, i)
		sift_down(i, 0, less, swap)
	}
}

func main() {
	var n int
	fmt.Scanf("%d", &n)
	arr := make([]int, n)
	for i := 0; i < n; i++ {
		fmt.Scanf("%d", &arr[i])
	}
	less := func(i, j int) bool { return arr[i] < arr[j] }
	swap := func(i, j int) { arr[i], arr[j] = arr[j], arr[i] }
	hsort(len(arr), less, swap)
	fmt.Println(arr)
}
