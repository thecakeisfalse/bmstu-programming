#include <algorithm>
#include <iomanip>
#include <iostream>
#include <math.h>
#include <vector>

using namespace std;

typedef pair<int, int> pii;
typedef vector<int> vi;

int p(int v, vi &_p) { return (_p[v] == v) ? v : p(_p[v], _p); }

void unite(int a, int b, vi &_p) {
    a = p(a, _p), b = p(b, _p);
    _p[a] = b;
}

int main() {
    cout << fixed << setprecision(2);
    int n;
    cin >> n;
    vector<pair<int, pii>> edges;

    vector<pii> a(n);
    for (auto &[l, r] : a)
        cin >> l >> r;

    for (int i = 0; i < n; ++i) {
        for (int j = i + 1; j < n; ++j) {
            int dx2 = pow(a[i].first - a[j].first, 2), dy2 = pow(a[i].second - a[j].second, 2);
            edges.push_back({dx2 + dy2, {i, j}});
        }
    }

    sort(edges.begin(), edges.end());

    vi _p(n);
    for (int i = 0; i < n; ++i)
        _p[i] = i;

    double dist = 0;
    for (auto e : edges) {
        int a = e.second.first, b = e.second.second;
        if (p(a, _p) != p(b, _p)) {
            unite(a, b, _p);
            dist += sqrt(e.first);
        }
    }

    cout << round(dist * 100) / 100 << "\n";
}