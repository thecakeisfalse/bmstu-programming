#include <iostream>
#include <vector>

using namespace std;

typedef vector<int> vi;
typedef vector<vi> mat;

template <typename T, typename E> class MealyAutomata {
  private:
    vector<vector<T>> delta;
    vector<vector<E>> lambda;
    int states_size;
    int input_size;

  public:
    MealyAutomata(int n, int m) : states_size(n), input_size(m) {
        delta.assign(n, vector<T>(m));
        lambda.assign(n, vector<E>(m));
    }

    istream &read(istream &is) {
        for (int i = 0; i < states_size; ++i) {
            for (int j = 0; j < input_size; ++j) {
                is >> delta[i][j];
            }
        }

        for (int i = 0; i < states_size; ++i) {
            for (int j = 0; j < input_size; ++j) {
                is >> lambda[i][j];
            }
        }
        return is;
    }

    ostream &print(ostream &os) {
        os << "digraph {\n";
        os << "\trankdir = LR\n";

        for (int i = 0; i < states_size; ++i) {
            for (int j = 0; j < input_size; ++j) {
                os << "\t" << i << " -> " << delta[i][j] << " [label = \"" << (char)('a' + j) << "("
                   << lambda[i][j] << ")" << "\"]\n";
            }
        }

        os << "}";
        return os;
    }
};

template <typename T, typename E> ostream &operator<<(ostream &os, MealyAutomata<T, E> &au) {
    return au.print(os);
}

template <typename T, typename E> istream &operator>>(istream &is, MealyAutomata<T, E> &au) {
    return au.read(is);
}

int main() {
    int n, m, q0;
    cin >> n >> m >> q0;

    MealyAutomata<int, string> au(n, m);

    cin >> au;
    cout << au << "\n";
}