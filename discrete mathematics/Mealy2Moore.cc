#include <bits/stdc++.h>

using namespace std;

template <class T>
auto &operator>>(istream &is, vector<T> &arr) {
    for (auto &i : arr) is >> i;
    return is;
}

int main() {
    int n, m, k, count = 0;

    cin >> n;
    vector<string> inp(n);
    cin >> inp >> m;
    vector<string> out(m);
    cin >> out >> k;
    vector delta(k, vector(n, 0)), phi(k, vector(n, 0));
    cin >> delta >> phi;

    map<pair<int, int>, int> b;
    for (int i = 0; i < n; ++i)
        for (int j = 0; j < k; ++j)
            b[{delta[j][i], phi[j][i]}];

    for (auto &[_, v] : b) v = count++;

    printf("digraph {\n"
           "\trankdir = LR\n");

    for (auto &[kv, c] : b) {
        auto &[k, v] = kv;
        printf("\t%d [label = \"(%d,%s)\"]\n", c, k, out[v].c_str());
    }

    for (auto &[kv, c] : b) {
        auto &[k, _] = kv;
        for (int i = 0; i < n; ++i) {
            printf("\t%d -> %d [label = \"%s\"]\n",
                   c, b[{delta[k][i], phi[k][i]}], inp[i].c_str());
        }
    }

    printf("}\n");
}

