#include <iostream>
#include <queue>
#include <vector>

using namespace std;

const int INF = 1e9;

typedef pair<int, int> pii;
typedef vector<int> vi;

int dijkstra(int n, const vi &g) {
    vi d(n * n, INF);
    d[0] = 0;

    priority_queue<pii, vector<pii>, greater<pii>> q;
    q.push({0, 0});

    vector<pii> shifts = {{-1, 0}, {1, 0}, {0, -1}, {0, 1}};

    while (!q.empty()) {
        auto [cur_d, v] = q.top();
        q.pop();
        if (cur_d > d[v])
            continue;

        int x = v % n, y = v / n;

        for (auto [dx, dy] : shifts) {
            int nx = x + dx, ny = y + dy;

            if (!(0 <= nx && nx < n) || !(0 <= ny && ny < n))
                continue;

            int u = nx + ny * n, w = g[u];
            if (d[u] > d[v] + w) {
                d[u] = d[v] + w;
                q.push({d[u], u});
            }
        }
    }

    return d.back() + g[0];
}

int main() {
    int n;
    cin >> n;
    vi g(n * n);
    for (auto &x : g)
        cin >> x;
    auto result = dijkstra(n, g);
    cout << result << "\n";
}