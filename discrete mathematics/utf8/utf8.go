package main

func significantBitsUTF8(s byte) int {
	for i := 0; i < 8; i++ {
		if s&(1<<(7-i)) == 0 {
			if i == 0 {
				return 1
			}
			return i
		}
	}
	return 0
}

func significantBitsUTF32(s rune) int {
	count := 0
	for ; s > 0; s /= 2 {
		count++
	}
	return count
}

func decode(utf8 []byte) []rune {
	answer := make([]rune, 0)

	for i := 0; i < len(utf8); {
		shift := significantBitsUTF8(utf8[i])
		var result rune
		if shift == 1 {
			result = rune(utf8[i])
		} else {
			result = rune(utf8[i] & (1<<(7-shift) - 1))
			for j := 1; j < shift; j++ {
				result <<= 6
				result |= rune(utf8[i+j] & (1<<6 - 1))
			}
		}

		answer = append(answer, result)

		i += shift
	}

	return answer
}

func encode(utf32 []rune) []byte {
	var (
		temp, answer []byte
		size         int
	)

	for _, v := range utf32 {
		shift := significantBitsUTF32(v)

		if shift <= 7 {
			size = 0
		} else if shift <= 11 {
			size = 1
		} else if shift <= 16 {
			size = 2
		} else if shift <= 21 {
			size = 3
		}

		if size == 0 {
			temp = []byte{byte(v & 127)}
		} else {
			temp = make([]byte, 0)
			for i := 0; i < size; i++ {
				temp = append(temp, 0b10000000|byte(v&63))
				v >>= 6
			}

			var header rune = (1<<(size+1) - 1) << (7 - size)
			temp = append(temp, byte(header|v))
		}

		for i := len(temp) - 1; i >= 0; i-- {
			answer = append(answer, temp[i])
		}
	}

	return answer
}

