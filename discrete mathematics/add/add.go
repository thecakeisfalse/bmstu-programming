package main

import "fmt"

func max(a, b int) int {
	if a > b {
		return a
	}
	return b
}

func read_long() []int32 {
	var n int
	fmt.Scanf("%d", &n)
	a := make([]int32, n)

	for i := 0; i < n; i++ {
		fmt.Scanf("%d", &a[i])
	}

	return a
}

func add(a, b []int32, p int) []int32 {
	n := max(len(a), len(b))
	result := make([]int32, n+1)

	for i := 0; i < n; i++ {
		if i < len(a) {
			result[i] += a[i]
		}

		if i < len(b) {
			result[i] += b[i]
		}

		result[i+1] = result[i] / int32(p)
		result[i] %= int32(p)
	}

	if len(result) > 0 && result[len(result)-1] == 0 {
		return result[:len(result)-1]
	}

	return result
}

func main() {
	var p int
	fmt.Scanf("%d", &p)
	a := read_long()
	b := read_long()

	r := add(a, b, p)

	for _, v := range r {
		fmt.Printf("%d", v)
	}
	fmt.Println("")
}
