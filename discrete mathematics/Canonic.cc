#include <iostream>
#include <vector>

using namespace std;

template <typename E> class MealyAutomata {
  private:
    vector<vector<int>> delta;
    vector<vector<E>> lambda;
    int states_size;
    int input_size;
    int default_state;

    void dfs(int v, vector<int> &used, int &idx) {
        used[v] = idx++;
        for (auto u : delta[v]) {
            if (used[u] == -1) {
                dfs(u, used, idx);
            }
        }
    }

  public:
    MealyAutomata(int n, int m, int q0 = 0) : states_size(n), input_size(m), default_state(q0) {
        delta.assign(n, vector<int>(m));
        lambda.assign(n, vector<E>(m));
    }

    void read() {
        for (int i = 0; i < states_size; ++i) {
            for (int j = 0; j < input_size; ++j) {
                cin >> delta[i][j];
            }
        }

        for (int i = 0; i < states_size; ++i) {
            for (int j = 0; j < input_size; ++j) {
                cin >> lambda[i][j];
            }
        }
    }

    void canonize() {
        vector<int> used(states_size, -1);
        int idx = 0;

        dfs(default_state, used, idx);

        vector<vector<int>> delta1(delta);
        vector<vector<E>> lambda1(lambda);

        for (int i = 0; i < states_size; ++i) {
            int q = used[i];
            for (int j = 0; j < input_size; ++j) {
                delta[q][j] = used[delta[q][j]];
            }
        }

        for (int i = 0; i < states_size; ++i) {
            int q = used[i];
            delta1[q] = delta[i];
            lambda1[q] = lambda[i];
        }

        delta = delta1, lambda = lambda1;
        default_state = 0;
    }

    void printDOT() {
        cout << "digraph {\n";
        cout << "\trankdir = LR\n";

        for (int i = 0; i < states_size; ++i) {
            for (int j = 0; j < input_size; ++j) {
                cout << "\t" << i << " -> " << delta[i][j] << " [label = \"" << (char)('a' + j)
                     << "(" << lambda[i][j] << ")" << "\"]\n";
            }
        }

        cout << "}\n";
    }

    void print() {
        cout << states_size << "\n";
        cout << input_size << "\n";
        cout << default_state << "\n";

        for (int i = 0; i < states_size; ++i) {
            for (int j = 0; j < input_size; ++j) {
                cout << delta[i][j] << " ";
            }
            cout << "\n";
        }

        for (int i = 0; i < states_size; ++i) {
            for (int j = 0; j < input_size; ++j) {
                cout << lambda[i][j] << " ";
            }
            cout << "\n";
        }
    }
};

int main() {
    int n, m, q0;
    cin >> n >> m >> q0;

    MealyAutomata<string> au(n, m, q0);

    au.read();
    au.canonize();
    au.print();
}