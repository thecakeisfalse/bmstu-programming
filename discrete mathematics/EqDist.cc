#include <cstdint>
#include <iomanip>
#include <iostream>
#include <queue>
#include <set>
#include <vector>

using namespace std;

typedef vector<int32_t> vi;
typedef vector<vi> Graph;

vi BFS(int32_t s, int32_t n, Graph g) {
    vi dist(n, -1);
    dist[s] = 0;

    queue<int> q;
    q.push(s);

    while (!q.empty()) {
        auto v = q.front();
        q.pop();
        for (auto u : g[v]) {
            if (dist[u] == -1) {
                dist[u] = dist[v] + 1;
                q.push(u);
            }
        }
    }

    return dist;
}

int main() {
    int32_t n, m;
    cin >> n >> m;

    Graph g(n);
    vector<pair<int32_t, int32_t>> edges;
    while (m-- > 0) {
        int u, v;
        cin >> u >> v;
        edges.push_back({u, v});
        g[u].push_back(v);
        g[v].push_back(u);
    }

    int32_t k;
    cin >> k;

    vector<bool> good(n, true);
    for (int i = 0; i < k; ++i) {
        int vi;
        cin >> vi;
        good[vi] = false;
    }

    vector<vi> a;
    for (int i = 0; i < n; ++i)
        if (!good[i])
            a.push_back(BFS(i, n, g));

    for (int i = 0; i < n; ++i) {
        for (int j = 1; j < a.size(); ++j) {
            if (a[j][i] != a[j - 1][i] || a[j][i] == -1 || a[j - 1][i] == -1) {
                good[i] = false;
                break;
            }
        }
    }

    int count = 0;
    for (int i = 0; i < n; ++i) {
        if (good[i]) {
            cout << i << " ";
            ++count;
        }
    }

    if (count == 0)
        cout << "-";

    cout << "\n";
}