#include <iostream>
#include <map>
#include <vector>

#define INF __INT32_MAX__

using namespace std;

struct Edge {
    Edge(int to_, int dist_) : to(to_), distance(dist_) {}

    int to;
    int distance;
};

typedef map<int, vector<Edge>> Graph;

int Prim(Graph graph, int n, int m) {
    bool used[n] = {0};
    int sum = 0, left = n - 1;

    used[0] = 1;
    for (int i = 0; i < n - 1; i++) {
        int distance = INF, to, from;
        for (int j = 0; j < n; j++)
            if (used[j])
                for (auto x : graph[j])
                    if (!used[x.to] && x.distance < distance)
                        to = x.to, from = j, distance = x.distance;

        used[to] = 1;
        sum += distance;
    }

    return sum;
}

int main() {
    // cin.tie(0);
    // ios_base::sync_with_stdio(0);

    Graph graph;

    int n, m;
    cin >> n >> m;

    for (int i = 0; i < m; i++) {
        int u, v, len;
        cin >> u >> v >> len;

        graph[u].push_back(Edge(v, len));
        graph[v].push_back(Edge(u, len));
    }

    cout << Prim(graph, n, m) << "\n";

    return 0;
}