package main

import (
	"bufio"
	"fmt"
	"os"
)

func econom(expr string) int {
	var (
		cache map[string]int
		stack []int
		j     int
	)
	stack = make([]int, 0)
	cache = map[string]int{}

	for i, v := range expr {
		if v == '(' {
			stack = append([]int{i}, stack...)
		} else if v == ')' {
			j, stack = stack[0], stack[1:]
			cache[expr[j:i+1]] = 1
		}
	}

	return len(cache)
}

func main() {
	reader := bufio.NewReader(os.Stdin)
	s, _, _ := reader.ReadLine()
	fmt.Printf("%d\n", econom(string(s)))
}
