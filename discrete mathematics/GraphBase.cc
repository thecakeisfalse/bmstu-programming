#include <algorithm>
#include <iostream>
#include <set>
#include <vector>

using namespace std;

typedef vector<int> vi;
typedef vector<bool> vb;
typedef vector<vi> Graph;

void dfs(int v, const Graph &g, vb &used) {
    used[v] = true;
    for (auto u : g[v])
        if (!used[u])
            dfs(u, g, used);
}

void dfs1(int v, const Graph &g, vb &used, vi &order) {
    used[v] = true;
    for (auto u : g[v])
        if (!used[u])
            dfs1(u, g, used, order);
    order.push_back(v);
}

void dfs2(int v, const Graph &gT, vb &used, vi &component) {
    used[v] = true;
    component.push_back(v);
    for (auto u : gT[v])
        if (!used[u])
            dfs2(u, gT, used, component);
}

int main() {
    int n, m;
    cin >> n >> m;
    Graph g(n), gT(n);

    while (m-- > 0) {
        int u, v;
        cin >> u >> v;
        g[u].push_back(v);
        gT[v].push_back(u);
    }

    vi order, component;

    vb used(n, false);
    for (int i = 0; i < n; ++i)
        if (!used[i])
            dfs1(i, g, used, order);

    reverse(order.begin(), order.end());

    used.assign(n, false);

    vector<int> temp_base;
    for (int i = 0; i < n; ++i) {
        if (!used[order[i]]) {
            dfs2(order[i], gT, used, component);
            temp_base.push_back(*min_element(component.begin(), component.end()));
            component.clear();
        }
    }

    used.assign(n, false);

    for (auto v : temp_base) {
        if (!used[v]) {
            dfs(v, g, used);
            used[v] = 0;
        }
    }

    sort(temp_base.begin(), temp_base.end());

    for (auto v : temp_base)
        if (!used[v])
            cout << v << ' ';

    cout << '\n';
}