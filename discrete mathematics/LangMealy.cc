#include <bits/stdc++.h>

using namespace std;

struct pair_hash {
    template <class T1, class T2>
    size_t operator () (pair<T1, T2> const &pair) const {
        size_t h1 = hash<T1>()(pair.first);
        size_t h2 = hash<T2>()(pair.second);
        return h1 ^ h2;
    }
};

using Automata = vector<vector<pair<int, string>>>;
using Cache = unordered_set<pair<int, string>, pair_hash>;
using Set = unordered_set<string>;

auto readMealy() {
    int n;
    cin >> n;

    Automata a(n, vector<pair<int, string>>(2));
    for (int i = 0; i < n; ++i)
        for (int j = 0; j < 2; ++j)
            cin >> a[i][j].first;

    for (int i = 0; i < n; ++i)
        for (int j = 0; j < 2; ++j)
            cin >> a[i][j].second;

    return a;
}

void genAllStrings(int q0, const Automata &a, Set &result, string s, int max_l, Cache &used) {
    if (used.find({q0, s}) != used.end()) return;

    used.emplace(q0, s);
    result.insert(s);

    if (s.size() < max_l) {
        for (int i = 0; i < 2; ++i) {
            auto [q, c] = a[q0][i];
            if (q != q0 || c != "-")
                genAllStrings(q, a, result, s + (c == "-" ? "" : c), max_l, used);
        }
    }
}

int main() {
    Cache used;
    Set result;
    string s = "";

    auto a = readMealy();
    int q0, ml;
    cin >> q0 >> ml;

    genAllStrings(q0, a, result, s, ml, used);
    result.erase("");
    for (auto & x : result) cout << x << '\n';
}
