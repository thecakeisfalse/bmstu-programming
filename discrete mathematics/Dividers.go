package main

import (
	"fmt"
	"sort"
)

func divs(n int) []int {
	k := make([]int, 0)
	for d := 1; d*d <= n; d++ {
		if n%d == 0 {
			k = append(k, d)
			if d*d != n {
				k = append(k, n/d)
			}
		}
	}
	sort.Sort(sort.Reverse(sort.IntSlice(k)))
	return k
}

func dividersGraph(n int) {
	d := divs(n)
	m := make([][]bool, len(d))
	for i := range m {
		m[i] = make([]bool, len(d))
	}

	for i := range d {
		for j := i + 1; j < len(d); j++ {
			if d[i]%d[j] == 0 {
				is_valid := true
				for k := i + 1; k < j; k++ {
					if d[i]%d[k] == 0 && d[k]%d[j] == 0 {
						is_valid = false
						break
					}
				}
				if is_valid {
					m[i][j] = true
				}
			}
		}
	}

	fmt.Println("graph {")

	for _, x := range d {
		fmt.Printf("\t%d\n", x)
	}

	for i := range d {
		for j := range d {
			if m[i][j] {
				fmt.Printf("\t%d--%d\n", d[i], d[j])
			}
		}
	}

	fmt.Println("}")
}

func main() {
	var n int
	fmt.Scanf("%d", &n)
	dividersGraph(n)
}
