#include <bits/stdc++.h>

using namespace std;

using Automata = vector<vector<pair<int, string>>>;
using Cache = vector<vector<bool>>;

auto readMealy() {
    int n, m, q0;
    cin >> n >> m >> q0;

    Automata a(n, vector<pair<int, string>>(m));
    for (int i = 0; i < n; ++i)
        for (int j = 0; j < m; ++j)
            cin >> a[i][j].first;

    for (int i = 0; i < n; ++i)
        for (int j = 0; j < m; ++j)
            cin >> a[i][j].second;
    
    return make_pair(q0, a);
}

bool dfs(int q1, Automata &a1, int q2, Automata &a2, Cache &used) {
    if (!used[q1][q2]) {
        used[q1][q2] = true;

        for (int i = 0; i < min(a1[0].size(), a2[0].size()); ++i) {
            auto [w1, c1] = a1[q1][i];
            auto [w2, c2] = a2[q2][i];
            if (c1 != c2 || !dfs(w1, a1, w2, a2, used))
                return false;
        }
    }
    return true;
}

int main() {
    auto [q1, a1] = readMealy();
    auto [q2, a2] = readMealy();
    Cache used(a1.size(), vector<bool>(a2.size()));
    cout << (dfs(q1, a1, q2, a2, used) ? "EQUAL" : "NOT EQUAL") << '\n';
}