#include <algorithm>
#include <iostream>
#include <vector>

typedef std::vector<int> array;
typedef std::vector<array> Graph;

void DFS(int v, const Graph &g, array &enter, array &ret, std::vector<bool> &visited, int time,
         int &result, int p = -1) {
    visited[v] = true;
    enter[v] = ret[v] = time++;

    for (const auto &u : g[v]) {
        if (u == p)
            continue;

        if (visited[u])
            ret[v] = std::min(ret[v], enter[u]);
        else {
            DFS(u, g, enter, ret, visited, time, result, v);
            ret[v] = std::min(ret[v], ret[u]);
            if (ret[u] > enter[v])
                ++result;
        }
    }
}

int main() {
    int n, m;
    std::cin >> n >> m;

    Graph g(n);
    while (m-- > 0) {
        int u, v;
        std::cin >> u >> v;
        g[u].push_back(v);
        g[v].push_back(u);
    }

    array enter(n), ret(n);
    std::vector<bool> visited(n, false);

    int result = 0;
    for (int i = 0; i < n; ++i)
        if (!visited[i])
            DFS(i, g, enter, ret, visited, 0, result);

    std::cout << result << "\n";
}