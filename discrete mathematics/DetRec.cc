#include <bits/stdc++.h>

using namespace std;

using elem = pair<int, string>;
using Automata = vector<vector<elem>>;
using Set = set<int>;

void Dfs(const Automata &a, set<int> &c, int q) {
    if (c.find(q) == c.end()) {
        c.insert(q);
        for (auto & [w, label] : a[q]) {
            if (label == "lambda") {
                Dfs(a, c, w);
            }
        }
    }
}

void Det(Automata &a, const vector<string> &x, const vector<int> &final, int q) {
    vector<Set> hist;
    vector<vector<Set>> delta(2*a.size());

    auto Closure = [&](const Set &z) {
        Set c;
        for (auto & q : z)
            Dfs(a, c, q);
        return c;
    };

    map<pair<int, int>, int> cache;
    auto Find = [&](int i, int j) {
        if (cache.find({i, j}) != cache.end()) {
            return cache[{i, j}];
        }
        auto z = delta[i][j];
        auto it = find(hist.begin(), hist.end(), z);
        return cache[{i, j}] = distance(hist.begin(), it);
    };

    Set q0 = Closure({q});
    vector<Set> s = {q0}, Q = {q0};

    printf("digraph {\n"
           "\trankdir = LR\n");

    while (!s.empty()) {
        auto z = s.back();
        s.pop_back();

        bool isFinal = any_of(z.begin(), z.end(), [&](int v) {
            return final[v] == 1;
        });

        hist.push_back(z);

        printf("\t%d [label=\"[", hist.size()-1);

        int i = 1;
        for (auto v : z) {
            printf("%d", v);
            if (i++ != z.size()) putchar(' ');
        }

        printf("]\", shape=%s]\n", (isFinal ? "doublecircle" : "circle"));

        for (auto &id : x) {
            Set W;
            for (auto q : z) {
                for (auto &[w, label] : a[q]) {
                    if (label == id) {
                        W.insert(w);
                    }
                }
            }

            Set z1 = Closure(W);
            auto it = find(Q.begin(), Q.end(), z1);

            if (it == Q.end()) {
                Q.push_back(z1);
                s.push_back(z1);
            }

            delta[hist.size()-1].push_back(z1);
        }
    }

    for (int i = 0; i < hist.size(); ++i) {
        map<int, int> table;
        for (int j = 0; j < x.size(); ++j) {
            if (table.find(Find(i, j)) == table.end()) {
                table[Find(i, j)] = 1;
                printf("\t%d -> %d [label=\"%s", i, Find(i, j), x[j].c_str());
                for (int k = j+1; k < x.size(); ++k) {
                    if (Find(i, j) == Find(i, k)) {
                        printf(", %s", x[k].c_str());
                    }
                }
                printf("\"]\n");
            }
        }
    }

    printf("}\n");
}

int main() {
    int n, m, u, v, q0;
    cin >> n >> m;

    Automata a(n);
    unordered_set<string> used;
    vector<string> x;
    for (int i = 0; i < m; ++i) {
        string s;
        cin >> u >> v >> s;
        if (s != "lambda" && used.find(s) == used.end()) {
            used.insert(s);
            x.push_back(s);
        }
        a[u].emplace_back(v, s);
    }

    vector<int> final(n);
    for (auto & f : final) cin >> f;
    cin >> q0;

    Det(a, x, final, q0);
}
