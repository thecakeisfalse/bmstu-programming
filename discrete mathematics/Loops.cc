#include <iostream>
#include <vector>
#include <unordered_map>

using namespace std;

using vi = vector<int>;

class Graph {
public:
    enum { ID, ANC, BEL, SDOM, DOM, P, DPH };

    int size, depth;
    vector<vi> adj, adjT;
    vector<vi> vert;

    Graph(int n) : size{n+1}, depth{0} {
        adj.assign(size, vi());
        adjT.assign(size, vi());
        vert.assign(7, vi(size));

        for (int i = 1; i <= n; ++i)
            vert[SDOM][i] = vert[BEL][i] = i;
    }

    void addEdge(int u, int v) {
        adj[u].push_back(v);
        adjT[v].push_back(u);
    }

    void dfs(int u) {
        vert[DPH][u] = ++depth;
        vert[ID][depth] = u;

        for (auto v : adj[u]) {
            if (!vert[DPH][v]) {
                vert[P][v] = u;
                dfs(v);
            }
        }
    }

    int searchAndCut(int u) {
        if (!vert[ANC][u])
            return u;
        
        int root = searchAndCut(vert[ANC][u]);
        int &a = vert[BEL][vert[ANC][u]],
            &b = vert[BEL][u];

        if (vert[DPH][vert[SDOM][a]] < vert[DPH][vert[SDOM][b]])
            b = a;

        return vert[ANC][u] = root;
    }

    int findMin(int u) {
        searchAndCut(u);
        return vert[BEL][u];
    }

    void dominators() {
        vector bucket(size, vi());

        for (int i = depth; i > 1; --i) {
            int u = vert[ID][i];

            for (auto v : adjT[u]) {
                if (!vert[DPH][v])
                    continue;

                auto m = findMin(v);

                int &x = vert[SDOM][m],
                    &y = vert[SDOM][u];

                if (vert[DPH][x] < vert[DPH][y])
                    y = x;
            }

            vert[ANC][u] = vert[P][u];
            bucket[vert[SDOM][u]].push_back(u);

            for (auto v : bucket[vert[P][u]]) {
                auto m = findMin(v);

                if (vert[SDOM][m] == vert[SDOM][v])
                    vert[DOM][v] = vert[P][u];
                else
                    vert[DOM][v] = m;
            }

            bucket[vert[P][u]].clear();
        }

        for (int i = 2; i <= depth; ++i) {
            int u = vert[ID][i];
            int &a = vert[DOM][u];
            if (a != vert[SDOM][u])
                a = vert[DOM][a];
        }
    }

    int countNaturalLoops() {
        dfs(1);
        dominators();

        int count = 0;
        for (int v = 1; v < size; ++v) {
            for (auto u : adjT[v]) {
                for (; u != v && u != 0; )
                    u = vert[DOM][u];

                if (u == v) {
                    ++count;
                    break;
                }
            }
        }

        return count;
    }
};

auto readGraph() {
    int n;
    cin >> n;

    Graph g(n);
    unordered_map<int, int> id;

    auto ver = [&](int name) {
        if (id.find(name) == id.end())
            id[name] = id.size()+1;
        return id[name];
    };

    vector<pair<int, string>> a(n);
    vector<int> temp(n);

    for (int i = 0; i < n; ++i) {
        auto &[name, action] = a[i];
        cin >> name >> action;
        if (action != "ACTION")
            cin >> temp[i];
        ver(name);
    }

    for (int i = 0; i < n; ++i) {
        auto &[name, action] = a[i];
        if (action != "ACTION")
            g.addEdge(ver(name), ver(temp[i]));

        if (i + 1 < n && action != "JUMP")
            g.addEdge(ver(name), ver(a[i + 1].first));
    }

    return g;
}

int main() {
    auto a = readGraph();
    auto ans = a.countNaturalLoops();
    cout << ans << '\n';
}