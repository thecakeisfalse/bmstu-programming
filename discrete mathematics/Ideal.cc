#include <algorithm>
#include <iostream>
#include <queue>
#include <vector>

using namespace std;

typedef pair<int, int> pii;
typedef vector<int> vi;
typedef vector<pii> vpii;

auto getPath(int s, int e, const vpii &paths) {
    vi p;

    for (int v = s; v != e;) {
        auto [u, c] = paths[v];
        p.push_back(c);
        v = u;
    }

    reverse(p.begin(), p.end());

    return p;
}

auto bfs(int s, int n, const vector<vpii> &g) {
    vi dist(n, 1e9);
    dist[s] = {0};

    // (parent, color)
    vpii paths(n, {-1, 1e9});
    paths[s] = {-1, -1};

    queue<int> q;
    q.push(s);

    while (!q.empty()) {
        int u = q.front();
        q.pop();

        for (auto [v, c] : g[u]) {
            if (dist[v] == dist[u] + 1) {
                auto p1 = getPath(u, s, paths);
                auto p2 = getPath(paths[v].first, s, paths);

                if (p1 < p2 || (p1 == p2 && c < paths[v].second)) {
                    paths[v] = {u, c};
                }
            }

            if (dist[v] > dist[u] + 1) {
                dist[v] = dist[u] + 1;
                paths[v] = {u, c};
                q.push(v);
            }
        }
    }

    return paths;
}

int main() {
    int n, m;
    cin >> n >> m;

    vector<vpii> g(n);
    for (int i = 0; i < m; ++i) {
        int u, v, c;
        cin >> u >> v >> c;
        g[u - 1].emplace_back(v - 1, c);
        g[v - 1].emplace_back(u - 1, c);
    }

    auto paths = bfs(0, n, g);

    auto res = getPath(n - 1, 0, paths);

    cout << res.size() << '\n';
    for (auto c : res)
        cout << c << ' ';
    cout << '\n';
}