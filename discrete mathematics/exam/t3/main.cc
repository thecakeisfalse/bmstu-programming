#include <bits/stdc++.h>

using namespace std;
using vi = vector<int>;
using Automata = vector<pair<char, vi>>;

constexpr int INF = 1e9;

auto readAutomata(int m) {
    int n, q0;
    cin >> n >> q0;
    Automata a(n);
    for (int i = 0; i < n; ++i) {
        cin >> a[i].first;
        a[i].second = vi(m);
        for (auto & x : a[i].second)
            cin >> x;
    }
    return make_pair(q0, a);
}

int diffAutomata(int q1, const Automata &a1, int q2, const Automata &a2) {
    int m = a1[0].second.size(), n = a1.size();

    queue<pair<int, int>> q;
    map<pair<int, int>, int> dist;
    for (int i = 0; i < n; ++i)
        for (int j = 0; j < n; ++j)
            dist[{i, j}] = INF;

    dist[{q1, q2}] = 0;
    q.emplace(q1, q2);

    while (!q.empty()) {
        auto [v1, v2] = q.front();
        q.pop();

        for (int i = 0; i < m; ++i) {
            int w1 = a1[v1].second[i],
                w2 = a2[v2].second[i];
            if (dist[{w1, w2}] > dist[{v1, v2}] + 1) {
                dist[{w1, w2}] = dist[{v1, v2}] + 1;
                q.emplace(w1, w2);
            }
        }
    }

    int mn = INF;
    for (auto &[k, v] : dist) {
        auto [a, b] = k;
        if (a1[a].first != a2[b].first)
            mn = min(mn, v);
    }

    return mn;
}

int main() {
    int m;
    cin >> m;
    auto [q1, a1] = readAutomata(m);
    auto [q2, a2] = readAutomata(m);
    int ans = diffAutomata(q1, a1, q2, a2);
    if (ans == INF) cout << "=" << '\n';
    else cout << ans << '\n';
}