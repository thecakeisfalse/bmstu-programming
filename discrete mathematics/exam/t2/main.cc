#include <bits/stdc++.h>

using namespace std;

constexpr int INF = 1e9;

using vi = vector<int>;
using Graph = vector<vi>;

bool hasCycle(const Graph &g) {
    vi in(g.size());
    queue<int> q;
    int count = 0;

    for (int u = 0; u < g.size(); ++u)
        for (auto v : g[u])
            ++in[v];

    for (int u = 0; u < g.size(); ++u) {
        if (in[u] == 0) {
            q.push(u);
        }
    }

    while (!q.empty()) {
        int u = q.front();
        q.pop();
        ++count;

        for (auto v : g[u]) {
            --in[v];

            if (in[v] == 0) {
                q.push(v);
            }
        }
    }

    return count != g.size();
}

void dfs(int u, vector<bool> &used, const Graph &g, queue<int> &st) {
    st.push(u);
    used[u] = true;

    for (auto v : g[u])
        if (!used[v])
            dfs(v, used, g, st);
}

int mdist(int s, Graph &g) {
    vi dist(g.size(), -INF);
    dist[s] = 1;

    queue<int> st;
    vector<bool> used(g.size());
    dfs(s, used, g, st);

    while (!st.empty()) {
        int u = st.front();
        st.pop();

        for (auto v : g[u]) {
            if (dist[v] < dist[u] + 1)
                dist[v] = dist[u] + 1;
        }
    }

    int mx = -INF;
    for (auto x : dist)
        mx = max(mx, x);

    return mx;
}

int main() {
    int n, c;
    cin >> n;

    Graph g(n, vi());
    vi k(n);
    for (int i = 0; i < n; ++i) {
        cin >> k[i];

        for (int j = 0; j < k[i]; ++j) {
            cin >> c;
            g[c-1].push_back(i);
        }
    }

    auto cycle = hasCycle(g);

    if (cycle) {
        cout << "cycle" << '\n';
    } else {
        int mx = -1;
        for (int u = 0; u < n; ++u)
            if (k[u] == 0)
                mx = max(mx, mdist(u, g));
        cout << mx << '\n';
    }
}
